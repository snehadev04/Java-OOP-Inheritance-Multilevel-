//  Define the base class 'calc' with basic arithmetic operations
public class calc 
{
    // Method to add two integers
    int add(int n1, int n2) 
    {
        return n1 + n2;
    }
    
    // Method to subtract the second integer from the first
    int sub(int n1, int n2) 
    {
        return n1 - n2;
    }
}


